import UIKit

public extension UIFont {
    enum Texts {
        static let bodyRegular = UIFont.systemFont(ofSize: 16, weight: .regular)
    }

    enum Cells {
        static let title = UIFont.systemFont(ofSize: 16, weight: .medium)
    }

    enum Buttons {
        static let main = UIFont.systemFont(ofSize: 16, weight: .medium)
    }
}
